    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // Initialize counters
    int lowerFormStudents = 0;
    int upperFormStudents = 0;
    int gaTutors = 0;
    int gbTutors = 0;
    int gcTutors = 0;
    int receptionists = 0;
    int upperFormSubjects = 0;
    int lowerFormSubjects = 0;
    
    try {
        // Read student_enroll.txt to count students and subjects
        BufferedReader studentReader = new BufferedReader(new FileReader("student_enroll.txt"));
        String studentLine;
        
        while ((studentLine = studentReader.readLine()) != null) {
            String[] studentData = studentLine.split(",");
            if (studentData.length >= 5 && studentData[0].startsWith("S")) {
                String educationLevel = studentData[4].trim();
                
                // Count students
                if (educationLevel.equalsIgnoreCase("Lower Form")) {
                    lowerFormStudents++;
                    
                    // Count LF subjects
                    for (int i = 1; i <= 3; i++) {
                        if (studentData.length > i && !studentData[i].trim().isEmpty()) {
                            lowerFormSubjects++;
                        }
                    }
                } else if (educationLevel.equalsIgnoreCase("Upper Form")) {
                    upperFormStudents++;
                    
                    // Count UF subjects
                    for (int i = 1; i <= 3; i++) {
                        if (studentData.length > i && !studentData[i].trim().isEmpty()) {
                            upperFormSubjects++;
                        }
                    }
                }
            }
        }
        studentReader.close();
        
        // Read tutor.txt to count tutors
        BufferedReader tutorReader = new BufferedReader(new FileReader("tutor.txt"));
        String tutorLine;
        
        while ((tutorLine = tutorReader.readLine()) != null) {
            String[] tutorData = tutorLine.split(",");
            if (tutorData.length >= 2 && tutorData[0].startsWith("T")) {
                String level = tutorData[1].trim();
                
                if (level.equalsIgnoreCase("GA")) {
                    gaTutors++;
                } else if (level.equalsIgnoreCase("GB")) {
                    gbTutors++;
                } else if (level.equalsIgnoreCase("GC")) {
                    gcTutors++;
                }
            }
        }
        tutorReader.close();
        
        // Read receptionist.txt to count receptionists
        BufferedReader receptionistReader = new BufferedReader(new FileReader("receptionist.txt"));
        String receptionistLine;
        
        while ((receptionistLine = receptionistReader.readLine()) != null) {
            if (receptionistLine.startsWith("R")) {
                receptionists++;
            }
        }
        receptionistReader.close();
        
        // Calculate expenses and income
        int totalExpenses = (gaTutors * 2800) + (gbTutors * 1300) + (gcTutors * 1200);
        int incomeFromLF = lowerFormStudents * 50 * lowerFormSubjects;
        int incomeFromUF = upperFormStudents * 60 * upperFormSubjects;
        int totalIncome = (incomeFromLF + incomeFromUF) - totalExpenses;
        
        // Display the report (you can modify this to show in your UI)
        String report = "Monthly Income Report:\n" +
                       "=======================\n" +
                       "Lower Form Students: " + lowerFormStudents + "\n" +
                       "Upper Form Students: " + upperFormStudents + "\n" +
                       "GA Tutors: " + gaTutors + "\n" +
                       "GB Tutors: " + gbTutors + "\n" +
                       "GC Tutors: " + gcTutors + "\n" +
                       "Receptionists: " + receptionists + "\n" +
                       "Lower Form Subjects: " + lowerFormSubjects + "\n" +
                       "Upper Form Subjects: " + upperFormSubjects + "\n" +
                       "Total Expenses: RM" + totalExpenses + "\n" +
                       "Income from Lower Form: RM" + incomeFromLF + "\n" +
                       "Income from Upper Form: RM" + incomeFromUF + "\n" +
                       "Total Income: RM" + totalIncome + "\n";
        
        JOptionPane.showMessageDialog(null, report, "Monthly Income Report", JOptionPane.INFORMATION_MESSAGE);
        
    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Error reading files: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_button3ActionPerformed

