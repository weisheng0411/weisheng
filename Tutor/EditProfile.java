    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to save changes?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        // 1. 获取输入资料
        String name = tfName.getText().trim();
        String username = tfUsername.getText().trim();
        String contact = tfContact.getText().trim();
        String email = tfEmail.getText().trim();

        // 2. 简单验证
        if (name.isEmpty() || username.isEmpty() || contact.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        try {
            File inputFile = new File("tutor.txt");
            File tempFile = new File("tutor_temp.txt");

            BufferedReader reader = new BufferedReader(new FileReader(inputFile));
            BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));

            String line;
            while ((line = reader.readLine()) != null) {
                String[] data = line.split(",");
                if (data[0].equals(currentuserID)) {
                    // 修改后的资料写入 temp file
                    data[1] = name;
                    data[2] = username;
                    data[4] = email;
                    data[5] = contact;
                    line = String.join(",", data);
                }
                writer.write(line);
                writer.newLine();
            }

            reader.close();
            writer.close();

            // 用修改后的文件覆盖原本的
            if (inputFile.delete()) {
                tempFile.renameTo(inputFile);
            }

            JOptionPane.showMessageDialog(this, "Profile updated successfully!");

            loadProfile();         // 重新填充 TextField
            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error updating profile.");
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnSaveActionPerformed

