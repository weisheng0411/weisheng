    private void loadProfile() {
        try (BufferedReader br = new BufferedReader(new FileReader("tutor.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 8 && parts[0].equals(currentuserID)) {
                    tfName.setText(parts[1]);         // 显示姓名
                    tfUsername.setText(parts[2]);     // 显示用户名
                    tfEmail.setText(parts[4]);        // 显示电邮
                    tfContact.setText(parts[5]);    // 显示电话（Contact）
                    break;
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Failed to load profile: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
