    private void loadTutorSubjects(String userID) {
  
    comsubject.removeAllItems();  // 清空旧的 subject
    try (BufferedReader br = new BufferedReader(new FileReader("tutor.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts.length >= 8 && parts[0].equals(userID)) {
                String subjects = parts[7].trim();  // 第 8 项是 subject（用 | 分隔）
                String[] subjectList = subjects.split("\\|");  // 记得用双反斜杠转义 |
                for (String subject : subjectList) {
                    comsubject.addItem(subject.trim());
                }
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}

    private void loadClassInfoFile(String userID){
            DefaultTableModel model = (DefaultTableModel) tableClassInfo.getModel();
    model.setRowCount(0); // 清空表格

    try (BufferedReader br = new BufferedReader(new FileReader("ClassInfo.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts.length >= 8) {
                String recordTutorID = parts[7].trim(); // 第8项是UserID
                if (recordTutorID.equals(userID)) {
                    model.addRow(new Object[] {
                        parts[0], // Class ID
                        parts[1], // Subject
                        parts[2], // Course Date
                        parts[3], // Course Time
                        parts[4], // Location
                        parts[5], // Fee
                        parts[6], // Tutor Name
                        parts[7]  // User ID (tutorID)
                    });
                }
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}

    
