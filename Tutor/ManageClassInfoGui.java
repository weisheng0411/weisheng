    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
    String subject = comsubject.getSelectedItem().toString().trim();
    String day = cbCourseDay.getSelectedItem().toString().trim();
    String month = cbCourseMonth.getSelectedItem().toString();
    String year = cbCourseYears.getSelectedItem().toString();
    String courseDate = year + "-" 
                      + (month.length()==1?"0"+month:month) + "-" 
                      + (day.length()==1?"0"+day:day);
    String time       = cbCourseTime.getSelectedItem().toString();
    String location   = cbLocation.getSelectedItem().toString();
    String fee = tfFee.getText().trim();
    String userID = currentuserID; 
    String tutorName = tfTutorName.getText().trim();

    
      // 2. Basic non-empty validation
    if (subject.equals("-{select one}-") || fee.isEmpty() || tutorName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields");
        return;
    }


    // 3. 自动生成 Class ID，如 C001、C002…
    DefaultTableModel model = (DefaultTableModel) tableClassInfo.getModel();
    int rowCount = model.getRowCount();
    String classID = String.format("C%03d", rowCount + 1);

    // 4. 检查是否重复（可选）
    for (int i = 0; i < rowCount; i++) {
        if (model.getValueAt(i, 0).toString().equals(classID)) {
            classID = String.format("C%03d", rowCount + 2); // 避免重复
        }
    }
    
     boolean conflict = false;
    if (!location.equalsIgnoreCase("Online")) {
        try (BufferedReader br = new BufferedReader(new FileReader("ClassInfo.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length < 7) continue;

                String existingDate     = parts[2];  // stored as yyyy-MM-dd
                String existingTime     = parts[3];
                String existingLocation = parts[4];

                // Only check conflicts when the stored location matches the chosen one
                if (existingLocation.equalsIgnoreCase(location)) {
                    // Conflict if same date
                    if (existingDate.equals(courseDate)) {
                        JOptionPane.showMessageDialog(this,
                            "Date conflict at the same location. Please choose another date.",
                            "Conflict", JOptionPane.WARNING_MESSAGE);
                        conflict = true;
                        break;
                    }
                    // Or conflict if same time
                    if (existingTime.equals(time)) {
                        JOptionPane.showMessageDialog(this,
                            "Time conflict at the same location. Please choose another time.",
                            "Conflict", JOptionPane.WARNING_MESSAGE);
                        conflict = true;
                        break;
                    }
                }
            }
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                "Error reading ClassInfo.txt!",
                "File Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    if (conflict) {
        return;  // 若检测到冲突，则直接返回
    }
    
    // 5. 添加到 JTable
    Object[] row = {
        classID,
        subject,
        courseDate,
        time,
        location,
        fee,
        tutorName,
        userID
    };
    model.addRow(row);

    // 6. 写入文件（追加方式）
     try (BufferedWriter bw = new BufferedWriter(new FileWriter("ClassInfo.txt", true))) {
        bw.write(String.join(",",
            classID, subject, courseDate, time, location, fee, tutorName, userID
        ));
        bw.newLine();
    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this,
            "Error writing to ClassInfo.txt!",
            "File error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 7. 清空输入字段
    tfFee.setText("");
    tfTutorName.setText("");
    comsubject.setSelectedIndex(0);

    // 8. 成功提示
    JOptionPane.showMessageDialog(this, "Class information added successfully.");
    }//GEN-LAST:event_btnADDActionPerformed

